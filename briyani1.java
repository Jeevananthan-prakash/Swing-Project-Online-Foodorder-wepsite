public void saveOrderToDatabase(String itemName) {
    try {
        Connection conn = fooddelivery.getconnection();
        String query = "INSERT INTO orders (item_name) VALUES (?)";
        PreparedStatement ps = conn.prepareStatement(query);
        ps.setString(1, itemName);
        int rows = ps.executeUpdate();
        if (rows > 0) {
            System.out.println(  itemName + " added to order");
        } else {
            System.out.println(" Failed to insert order");
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
}


