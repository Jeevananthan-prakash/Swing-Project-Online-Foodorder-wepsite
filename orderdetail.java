    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     // Called when label is clickedpublic void insertOrderToDatabase() {
    String name= Name.getText();
    String mobile = Number.getText();
    String email = Mail.getText();
    String address = Address.getText();
    String foodItem = Item.getText();
    int quantity = (int) Quantity.getValue();
    String priceStr = Actualprice.getText();

    if (name.isEmpty() || mobile.isEmpty() || address.isEmpty() || foodItem.isEmpty() || priceStr.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please fill all required fields.", "Validation", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        double price = Double.parseDouble(priceStr);
        Connection con = fooddelivery.getconnection(); // your method to get DB connection
        String query = "INSERT INTO order_details (customer_name, mobile_number, email, address, food_item, quantity, price) VALUES (?, ?, ?, ?, ?, ?, ?)";
        PreparedStatement ps = con.prepareStatement(query);
        ps.setString(1, name);
        ps.setString(2, mobile);
        ps.setString(3, email);
        ps.setString(4, address);
        ps.setString(5, foodItem);
        ps.setInt(6, quantity);
        ps.setDouble(7, price);

        int result = ps.executeUpdate();
        if (result > 0) {
            JOptionPane.showMessageDialog(null, " Order Confirmed and Stored!");
        } else {
            JOptionPane.showMessageDialog(null, " Order failed to store.");
        }

        ps.close();
        con.close();
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Invalid price format.");
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Database error: " + e.getMessage());
    }
    
    fin v=new fin();
    v.setVisible(true);
    this.setVisible(false); 
    
    


    


                                       


   

    }//GEN-LAST:event_jButton1ActionPerformed

